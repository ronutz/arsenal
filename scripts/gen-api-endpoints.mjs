// ============================================================================
// scripts/gen-api-endpoints.mjs
// ----------------------------------------------------------------------------
// Writes src/generated/api-endpoints.ts: a map from tool slug to the tool's
// public API endpoint metadata, derived from the freshly-built OpenAPI spec.
//
// WHY read the spec (not the slug): the operationId is what Swagger UI uses as
// its deep-link target, and it is NOT always derivable from the slug. Most
// tools use "run_<slug>", but hand-authored operations differ (e.g. /cidr's
// operationId is "computeCidr"). Reading public/openapi.json makes this map
// always correct, and self-maintaining as the spec changes.
//
// ORDER: this runs in prebuild AFTER scripts/build-openapi.mts (which writes
// public/openapi.{json,yaml}), so the spec it reads is current.
//
// The emitted map drives the "endpoint URL" affordance on each API-capable
// tool page: the URL to show, the HTTP method, and the { op, tag } pair used to
// build a Swagger UI deep link (#/<tag>/<operationId>) on the /api page. The
// API itself is NOT served (documentation only); these are reference links.
// ============================================================================

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";

const spec = JSON.parse(readFileSync("public/openapi.json", "utf8"));

// The server URL is the API base, e.g. "https://ronutz.com/api/v1".
const base = (spec.servers?.[0]?.url ?? "").replace(/\/+$/, "");

/** @type {Record<string, { url: string, method: string, op: string, tag: string }>} */
const map = {};

for (const [path, ops] of Object.entries(spec.paths ?? {})) {
  const slug = path.replace(/^\//, "");
  // Prefer the GET operation for the deep-link anchor: every tool exposes GET,
  // and it is the simplest call shape. Fall back to whatever method exists.
  const getOp = ops.get ?? Object.values(ops)[0];
  if (!getOp?.operationId) continue;
  // A tool is "structured" (JSON body) when it only makes sense over POST; we
  // surface GET as the canonical documented method here, but note POST-only
  // tools by method when GET is absent.
  const method = ops.get ? "GET" : "POST";
  map[slug] = {
    url: `${base}/${slug}`,
    method,
    op: getOp.operationId,
    tag: getOp.tags?.[0] ?? "Tools",
  };
}

const body =
  `// AUTO-GENERATED at build time by scripts/gen-api-endpoints.mjs. Do not edit by hand.\n` +
  `// Source of truth: public/openapi.json (itself generated by scripts/build-openapi.mts).\n` +
  `//\n` +
  `// Maps a tool slug to its public API endpoint metadata. Used to show the\n` +
  `// endpoint URL on API-capable tool pages, linking to the Swagger UI view on\n` +
  `// the /api page (deep-linked to the tool's operation). The API is documented\n` +
  `// but NOT served from the site; these are reference links.\n` +
  `export interface ApiEndpoint {\n` +
  `  /** Full endpoint URL, e.g. "https://ronutz.com/api/v1/cidr". */\n` +
  `  url: string;\n` +
  `  /** Canonical documented HTTP method ("GET", or "POST" for body-only tools). */\n` +
  `  method: string;\n` +
  `  /** OpenAPI operationId (the Swagger UI deep-link target). */\n` +
  `  op: string;\n` +
  `  /** OpenAPI tag the operation lives under (the Swagger UI section). */\n` +
  `  tag: string;\n` +
  `}\n\n` +
  `export const API_ENDPOINTS: Record<string, ApiEndpoint> = ${JSON.stringify(map, null, 2)};\n\n` +
  `/** Endpoint metadata for a slug, or null if the tool has no API endpoint. */\n` +
  `export function apiEndpointFor(slug: string): ApiEndpoint | null {\n` +
  `  return API_ENDPOINTS[slug] ?? null;\n` +
  `}\n`;

mkdirSync("src/generated", { recursive: true });
writeFileSync("src/generated/api-endpoints.ts", body);
console.log(`[gen-api-endpoints] wrote ${Object.keys(map).length} endpoints from public/openapi.json`);
