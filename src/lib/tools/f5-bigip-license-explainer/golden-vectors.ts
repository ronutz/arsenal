export interface VectorFailure {
  id: string;
  reason: string;
}

/** Resolve a dotted check path against the parsed result (see checks docs). */
function getPath(root: unknown, path: string): unknown {
  let cur: unknown = root;
  for (const seg of path.split(".")) {
    if (cur === null || cur === undefined) return cur;
    if (seg.startsWith("token:")) {
      const key = seg.slice("token:".length);
      const tokens = (cur as { tokens?: LicenseToken[] }).tokens ?? [];
      cur = tokens.find((t) => t.key === key);
      continue;
    }
    cur = (cur as Record<string, unknown>)[seg];
  }
  return cur;
}

/** Run every vector; return a pass/fail report (build/CI + standalone use). */
export function verifyVectors(): { passed: number; failed: number; failures: VectorFailure[] } {
  const failures: VectorFailure[] = [];
  let passed = 0;

  for (const v of LICENSE_GOLDEN_VECTORS) {
    try {
      const got = run(v.input);
      let ok = true;
      for (const [path, expected] of Object.entries(v.checks)) {
        const actual = getPath(got, path);
        if (actual !== expected) {
          failures.push({ id: v.id, reason: `${path}: got ${JSON.stringify(actual)}, want ${JSON.stringify(expected)}` });
          ok = false;
          break;
        }
      }
      if (ok) passed++;
    } catch (err) {
      failures.push({ id: v.id, reason: `threw ${(err as Error).message}` });
    }
  }

  for (const v of LICENSE_REJECT_VECTORS) {
    try {
      run(v.input);
      failures.push({ id: v.id, reason: "expected throw, got result" });
    } catch (err) {
      if (err instanceof BigipLicenseError && err.code === v.expectCode) passed++;
      else failures.push({ id: v.id, reason: `wrong error: ${(err as Error).message}` });
    }
  }

  return { passed, failed: failures.length, failures };
}
