//
// The two fixtures below are structure-faithful DISTILLATIONS of two real
// license files provided by the site author from lab systems (2026-07-03):
// one BIG-IQ-managed, one licensed directly on the BIG-IP. Every line shape,
// section, repeat-key pattern, and value variant observed in the originals is
// preserved; ALL registration keys, module keys, and cryptographic blobs are
// MASKED (AAAAA-... / short stand-ins) so nothing sensitive ever ships in the
// repository. The Service Check Date values are kept verbatim because the
// K7727 verdict they pin (via the sibling f5-service-check-date engine's
// vendored table) is part of what these vectors prove:
//   * 20240218 -> newest reachable 17.1.x (LCD 2023-02-08), blocked from
//     17.5.x (LCD 2025-02-12);
//   * 20171122 -> newest reachable 13.1.x (LCD 2017-09-12), blocked from
//     14.0.x (LCD 2018-07-11).
//
// Checks are dotted paths into the parsed result (with a token:<key> selector
// for the flat token list), so each vector reads as a compact spec of what
// the fixture must decode to.
// ============================================================================

